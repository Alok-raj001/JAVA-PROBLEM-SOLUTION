
public class ArrayEx4 {
	public static void main(String[] args) {
		int a[]=new int[] {6,8,5,4};
		for (int i : a) {
			System.out.println(i);
		}
		
		double b[]=new double[] {2.3,5,.8,7.1};
		for (double i : b) {
			System.out.println(i);
		}
		
		String c[]=new String[] {"Hello","Hi","Ok"};
		for (String i : c) {
			System.out.println(i);
		}
	}
}
